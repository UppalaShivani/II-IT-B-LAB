/* 1.program on working of Arthematic operators */
#include <iostream>
using namespace std;
int main()
{
    int x,y;
    cout<<"enter two values: ";
    cin >>x>>y;
    cout<<"given numbers are  "<<x<<" and  "<<y<<endl;
    cout<<"addition of given numbers is : "<<x+y << endl;
    cout<<"subtraction of given numbers is : "<<x-y<<endl;
    cout<<"multiplication of given numbers is : "<<x*y<<endl;
    cout<<"dividion of given numbers is : "<<x/y<<"\n";
    cout<<"modular division of given numbers is : "<<x%y<<endl;
    return 0;
}